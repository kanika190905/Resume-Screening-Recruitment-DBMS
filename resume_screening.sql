CREATE OR REPLACE PROCEDURE calculate_candidate_score(
    app_id IN NUMBER,
    rec_id IN NUMBER,
    p_score OUT NUMBER
) IS
    exp_score NUMBER(5,2) := 0;
    skill_score NUMBER(5,2) := 0;
    edu_score NUMBER(5,2) := 0;
    cert_score NUMBER(5,2) := 0;
    job_exp NUMBER := 0;
    job_req_exp NUMBER := 0;
    v_skill_count NUMBER := 0;
    v_job_skill_count NUMBER := 0;
    edu_count NUMBER := 0;
    cert_count NUMBER := 0;
    v_decision VARCHAR2(20);
BEGIN

    SELECT NVL(MAX(j.required_exp), 0) INTO job_req_exp
    FROM Job j JOIN Job_Application ja ON j.job_id = ja.job_id WHERE ja.application_id = app_id;

    SELECT NVL(MAX(r.yrs_of_experience), 0) INTO job_exp
    FROM Resume r JOIN Job_Application ja ON r.candidate_id = ja.candidate_id WHERE ja.application_id = app_id;

    IF job_exp >= job_req_exp THEN 
        exp_score := 40;
    ELSIF job_req_exp > 0 THEN 
        exp_score := (job_exp / job_req_exp) * 40; 
    END IF;

    SELECT COUNT(*) INTO v_skill_count
    FROM Resume_Skills rs 
    JOIN Resume r ON rs.resume_id = r.resume_id
    JOIN Job_Application ja ON r.candidate_id = ja.candidate_id
    JOIN Job_Skills js ON ja.job_id = js.job_id AND rs.skill_id = js.skill_id
    WHERE ja.application_id = app_id;

    SELECT COUNT(*) INTO v_job_skill_count
    FROM Job_Skills js 
    JOIN Job_Application ja ON js.job_id = ja.job_id 
    WHERE ja.application_id = app_id;

    IF v_job_skill_count > 0 THEN 
        skill_score := (v_skill_count / v_job_skill_count) * 30; 
    ELSE 
        skill_score := 0; 
    END IF;

   
    SELECT COUNT(*) INTO edu_count
    FROM Education e 
    JOIN Resume r ON e.resume_id = r.resume_id 
    JOIN Job_Application ja ON r.candidate_id = ja.candidate_id
    WHERE ja.application_id = app_id;
    
    edu_score := LEAST(edu_count, 5) * 4;

 
    SELECT COUNT(*) INTO cert_count
    FROM Resume_Certifications rc 
    JOIN Resume r ON rc.resume_id = r.resume_id 
    JOIN Job_Application ja ON r.candidate_id = ja.candidate_id
    WHERE ja.application_id = app_id;
    
    cert_score := LEAST(cert_count, 5) * 2;

    p_score := exp_score + skill_score + edu_score + cert_score;


    IF p_score >= 70 THEN 
        v_decision := 'Shortlisted'; 
    ELSIF p_score >= 50 THEN 
        v_decision := 'On Hold'; 
    ELSE 
        v_decision := 'Rejected'; 
    END IF;

    MERGE INTO Screening_Result sr
    USING (SELECT rec_id AS recruiter_id, app_id AS application_id, p_score AS score, v_decision AS decision FROM dual) src
    ON (sr.recruiter_id = src.recruiter_id AND sr.application_id = src.application_id)
    WHEN MATCHED THEN UPDATE SET score = src.score, decision = src.decision
    WHEN NOT MATCHED THEN INSERT (recruiter_id, application_id, score, decision) VALUES (src.recruiter_id, src.application_id, src.score, src.decision);

    IF p_score >= 60 THEN 
        UPDATE Job_Application SET application_status = 'Review' WHERE application_id = app_id;
    ELSIF p_score >= 50 THEN
        UPDATE Job_Application SET application_status = 'Pending' WHERE application_id = app_id;
    ELSE 
        UPDATE Job_Application SET application_status = 'Rejected' WHERE application_id = app_id; 
    END IF;
    
    COMMIT;
END;
/

CREATE OR REPLACE PROCEDURE get_top_skills(top_n IN NUMBER, p_cursor OUT SYS_REFCURSOR) IS
BEGIN
    OPEN p_cursor FOR
        SELECT s.skill_name, COUNT(rs.resume_id) AS count
        FROM Skills s JOIN Resume_Skills rs ON s.skill_id = rs.skill_id GROUP BY s.skill_name ORDER BY count DESC FETCH FIRST top_n ROWS ONLY;
END;
