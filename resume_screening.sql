--------------------------------------------------------
-- 1. DATABASE SCHEMA (DDL)
--------------------------------------------------------
CREATE TABLE Candidate (
    candidate_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name VARCHAR2(50) NOT NULL,
    middle_name VARCHAR2(50),
    last_name VARCHAR2(50) NOT NULL,
    h_no VARCHAR2(20) NOT NULL,
    street_name VARCHAR2(100) NOT NULL,
    city VARCHAR2(50) NOT NULL,
    state VARCHAR2(50) NOT NULL,
    country VARCHAR2(50) NOT NULL,
    pincode NUMBER(6) NOT NULL,
    dob DATE NOT NULL
);

CREATE TABLE Candidate_email (
    candidate_id NUMBER,
    email_id VARCHAR2(100),
    PRIMARY KEY(candidate_id, email_id),
    FOREIGN KEY(candidate_id) REFERENCES Candidate(candidate_id) ON DELETE CASCADE,
    CONSTRAINT chk_email CHECK(email_id LIKE '%@%.%')
);

CREATE TABLE Candidate_phone (
    candidate_id NUMBER,
    phone_no VARCHAR2(15),
    PRIMARY KEY(candidate_id, phone_no),
    FOREIGN KEY(candidate_id) REFERENCES Candidate(candidate_id) ON DELETE CASCADE
);

CREATE TABLE Resume (
    resume_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    candidate_id NUMBER,
    yrs_of_experience NUMBER CHECK (yrs_of_experience >= 0),
    summary VARCHAR2(4000),
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY(candidate_id) REFERENCES Candidate(candidate_id) ON DELETE CASCADE
);

CREATE TABLE Education (
    education_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    resume_id NUMBER,
    degree VARCHAR2(100) NOT NULL,
    field_of_study VARCHAR2(100) NOT NULL,
    start_yr NUMBER,
    end_yr NUMBER,
    university_name VARCHAR2(150) NOT NULL,
    cgpa NUMBER(4,2) CHECK (cgpa >= 0.0 AND cgpa <= 10.0),
    FOREIGN KEY(resume_id) REFERENCES Resume(resume_id) ON DELETE CASCADE
);

CREATE TABLE Work_Experience (
    work_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    resume_id NUMBER,
    company_name VARCHAR2(100) NOT NULL,
    job_title VARCHAR2(100) NOT NULL,
    start_date DATE,
    end_date DATE,
    responsibilities VARCHAR2(4000),
    FOREIGN KEY (resume_id) REFERENCES Resume(resume_id) ON DELETE CASCADE
);

CREATE TABLE Technology (
    technology_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tech_name VARCHAR2(100) NOT NULL UNIQUE
);

CREATE TABLE Projects (
    project_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    resume_id NUMBER,
    project_title VARCHAR2(200) NOT NULL,
    description VARCHAR2(4000),
    FOREIGN KEY (resume_id) REFERENCES Resume(resume_id) ON DELETE CASCADE
);

CREATE TABLE Project_Technologies (
    project_id NUMBER,
    technology_id NUMBER,
    PRIMARY KEY (project_id, technology_id),
    FOREIGN KEY(project_id) REFERENCES Projects(project_id) ON DELETE CASCADE,
    FOREIGN KEY(technology_id) REFERENCES Technology(technology_id) ON DELETE CASCADE
);

CREATE TABLE Skills (
    skill_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    skill_name VARCHAR2(100) NOT NULL UNIQUE
);

CREATE TABLE Resume_Skills (
    resume_id NUMBER,
    skill_id NUMBER,
    PRIMARY KEY(resume_id, skill_id),
    FOREIGN KEY (resume_id) REFERENCES Resume(resume_id) ON DELETE CASCADE,
    FOREIGN KEY (skill_id) REFERENCES Skills(skill_id) ON DELETE CASCADE
);

CREATE TABLE Certifications (
    certification_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    certification_name VARCHAR2(200) NOT NULL,
    issuing_organisation VARCHAR2(200) NOT NULL
);

CREATE TABLE Resume_Certifications (
    resume_id NUMBER,
    certification_id NUMBER,
    issue_date DATE,
    PRIMARY KEY(resume_id, certification_id),
    FOREIGN KEY(resume_id) REFERENCES Resume(resume_id) ON DELETE CASCADE,
    FOREIGN KEY(certification_id) REFERENCES Certifications(certification_id) ON DELETE CASCADE
);

CREATE TABLE Recruiter (
    recruiter_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    company VARCHAR2(100) NOT NULL,
    email_id VARCHAR2(100) NOT NULL UNIQUE,
    CONSTRAINT recruiter_chk_email CHECK(email_id LIKE '%@%.%')
);

CREATE TABLE Job (
    job_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    recruiter_id NUMBER,
    job_title VARCHAR2(100) NOT NULL,
    job_description VARCHAR2(4000) NOT NULL,
    required_exp NUMBER CHECK (required_exp >= 0),
    location VARCHAR2(100) NOT NULL,
    FOREIGN KEY(recruiter_id) REFERENCES Recruiter(recruiter_id) ON DELETE CASCADE
);

CREATE TABLE Job_Skills (
    job_id NUMBER,
    skill_id NUMBER,
    PRIMARY KEY (job_id, skill_id),
    FOREIGN KEY (job_id) REFERENCES Job(job_id) ON DELETE CASCADE,
    FOREIGN KEY (skill_id) REFERENCES Skills(skill_id) ON DELETE CASCADE
);

CREATE TABLE Job_Application (
    application_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    candidate_id NUMBER,
    job_id NUMBER,
    application_status VARCHAR2(20) DEFAULT 'Pending' CHECK (application_status IN ('Pending', 'Review', 'Accepted', 'Rejected')),
    application_date DATE,
    FOREIGN KEY(candidate_id) REFERENCES Candidate(candidate_id) ON DELETE CASCADE,
    FOREIGN KEY(job_id) REFERENCES Job(job_id) ON DELETE CASCADE
);

CREATE TABLE Screening_Result (
    recruiter_id NUMBER,
    application_id NUMBER,
    score NUMBER(5,2) CHECK (score BETWEEN 0 AND 100),
    decision VARCHAR2(20) CHECK (decision IN ('Shortlisted', 'Rejected', 'On Hold')),
    PRIMARY KEY(recruiter_id, application_id),
    FOREIGN KEY(recruiter_id) REFERENCES Recruiter(recruiter_id) ON DELETE CASCADE,
    FOREIGN KEY(application_id) REFERENCES Job_Application(application_id) ON DELETE CASCADE
);

CREATE TABLE Interview (
    interview_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    application_id NUMBER,
    interview_date TIMESTAMP,
    interview_status VARCHAR2(20) DEFAULT 'Scheduled' CHECK (interview_status IN ('Scheduled', 'Completed', 'Cancelled')),
    FOREIGN KEY(application_id) REFERENCES Job_Application(application_id) ON DELETE CASCADE
);

