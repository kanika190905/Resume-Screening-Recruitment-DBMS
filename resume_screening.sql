CREATE OR REPLACE TRIGGER validate_candidate_age
BEFORE INSERT ON Candidate
FOR EACH ROW
DECLARE
    v_age NUMBER;
BEGIN
    v_age := TRUNC(MONTHS_BETWEEN(SYSDATE, :NEW.dob) / 12);
    IF v_age < 18 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Candidate must be at least 18 years old');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER validate_email_format
BEFORE INSERT ON Candidate_email
FOR EACH ROW
BEGIN
    IF :NEW.email_id NOT LIKE '%@%.%' THEN
        RAISE_APPLICATION_ERROR(-20002, 'Email must be in valid format (user@domain.com)');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER validate_phone_format
BEFORE INSERT ON Candidate_phone
FOR EACH ROW
BEGIN
    IF NOT REGEXP_LIKE(:NEW.phone_no, '^[0-9]{10,15}$') THEN
        RAISE_APPLICATION_ERROR(-20003, 'Phone must be 10-15 digits');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER update_resume_timestamp
BEFORE UPDATE ON Resume
FOR EACH ROW
BEGIN
    :NEW.last_updated := CURRENT_TIMESTAMP;
END;
/

CREATE OR REPLACE TRIGGER validate_application_status
BEFORE UPDATE ON Job_Application
FOR EACH ROW
BEGIN
    IF :OLD.application_status = 'Rejected' AND :NEW.application_status != 'Rejected' THEN
        RAISE_APPLICATION_ERROR(-20004, 'Cannot change status from Rejected');
    END IF;
    IF :OLD.application_status = 'Accepted' AND :NEW.application_status != 'Accepted' THEN
        RAISE_APPLICATION_ERROR(-20005, 'Cannot change status from Accepted');
    END IF;
END;
/

