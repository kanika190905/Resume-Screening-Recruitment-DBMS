SELECT 
    CASE 
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 22 THEN '18-21'
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 26 THEN '22-25'
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 31 THEN '26-30'
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 36 THEN '31-35'
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 41 THEN '36-40'
        ELSE '41+'
    END AS age_group,
    COUNT(*) AS count
FROM Candidate
GROUP BY 
    CASE 
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 22 THEN '18-21'
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 26 THEN '22-25'
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 31 THEN '26-30'
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 36 THEN '31-35'
        WHEN TRUNC(MONTHS_BETWEEN(SYSDATE, dob)/12) < 41 THEN '36-40'
        ELSE '41+'
    END
ORDER BY age_group;

